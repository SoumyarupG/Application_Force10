            }
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnCancel_Click(object sender, EventArgs e)
        {
            tbMac.ActiveTabIndex = 0;
            EnableControl(false);
            StatusButton("View");
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnSave_Click(object sender, EventArgs e)
        {
            string vStateEdit = Convert.ToString(ViewState["StateEdit"]);
            if (vStateEdit == "Add" || vStateEdit == null)
                vStateEdit = "Save";
            if (SaveRecords(vStateEdit) == true)
            {
                gblFuction.MsgPopup(gblMarg.SaveMsg);
                LoadGrid();
                StatusButton("View");
                ViewState["StateEdit"] = null;
            }
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="pPgIndx"></param>
        private void LoadGrid()
        {
            DataTable dt = null;
            CLogin oLg = null;
            try
            {
                oLg = new CLogin();
                dt = oLg.GetMacList();
                gvList.DataSource = dt.DefaultView;
                gvList.DataBind();
            }
            finally
            {
                oLg = null;
                dt = null;
            }
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void gvList_RowCommand(object sender, GridViewCommandEventArgs e)
        {
            Int32 pTblId = 0, vRow = 0;
            DataTable dt = null;
            CLogin oLG = null;
            try
            {
                pTblId = Convert.ToInt32(e.CommandArgument);
                ViewState["TblId"] = pTblId;
                if (e.CommandName == "cmdShow")
                {
                    GridViewRow gvRow = (GridViewRow)(((LinkButton)e.CommandSource).NamingContainer);
                    LinkButton btnShow = (LinkButton)gvRow.FindControl("btnShow");
                    foreach (GridViewRow gr in gvList.Rows)
                    {
                        LinkButton lb = (LinkButton)gr.FindControl("btnShow");
                        lb.ForeColor = System.Drawing.Color.Black;
                    }
                    btnShow.ForeColor = System.Drawing.Color.Red;
                    oLG = new CLogin();
                    dt = oLG.GetMacDetails(pTblId);
                    if (dt.Rows.Count > 0)
                    {
                        ddlBranch.SelectedIndex = ddlBranch.Items.IndexOf(ddlBranch.Items.FindByValue(dt.Rows[vRow]["BranchCode"].ToString()));
                        txtCompName.Text = Convert.ToString(dt.Rows[vRow]["ComputerName"]);
                        txtSlNo.Text = Convert.ToString(dt.Rows[vRow]["ProcessorId"]);
                        if (Convert.ToString(dt.Rows[vRow]["IsAuthorized"]) == "Y")
                            chkAuthorised.Checked = true;
                        else
                            chkAuthorised.Checked = false;
                        tbMac.ActiveTabIndex = 1;
                        StatusButton("Show");
                    }
                }
            }
            finally
            {
                dt = null;
                //CLogin = null;
            }
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="Mode"></param>
        /// <returns></returns>
        private Boolean SaveRecords(string Mode)
        {
            Boolean vResult = false;
            string vStateEdit = Convert.ToString(ViewState["StateEdit"]);
            string vIsAuthorised = "N";
            Int32 vErr = 0, vRec = 0, vTblId = 0;
            CLogin oLg = null;
            CGblIdGenerator oGbl = null;
            try
            {
                if (ViewState["TblId"] != null) vTblId = Convert.ToInt32(ViewState["TblId"]);
                if (chkAuthorised.Checked == true) vIsAuthorised = "Y";
                if (Mode == "Save")
                {
                    oLg = new CLogin();
                    vErr = oLg.SaveMAC(vTblId, txtSlNo.Text.Replace("'", "''"), txtCompName.Text.Replace("'", "''"), ddlBranch.SelectedValue, vIsAuthorised, "Save");
                    if (vErr > 0)
                    {
                        vResult = true;
                    }
                    else
                    {
                        gblFuction.MsgPopup(gblMarg.DBError);
                        vResult = false;
                    }
                }
                else if (Mode == "Edit")
                {
                    oLg = new CLogin();
                    vErr = oLg.SaveMAC(vTblId, txtSlNo.Text.Replace("'", "''"), txtCompName.Text.Replace("'", "''"), ddlBranch.SelectedValue, vIsAuthorised, "Edit");
                    if (vErr > 0)
                    {
                        gblFuction.MsgPopup(gblMarg.EditMsg);
                        vResult = true;
                    }
                    else
                    {
                        gblFuction.MsgPopup(gblMarg.DBError);
                        vResult = false;
                    }
                }
                else if (Mode == "Delete")
                {
                    oLg = new CLogin();
                    vErr = oLg.SaveMAC(vTblId, txtSlNo.Text.Replace("'", "''"), txtCompName.Text.Replace("'", "''"), ddlBranch.SelectedValue, vIsAuthorised, "Delet");
                    if (vErr > 0)
                    {
                        gblFuction.MsgPopup(gblMarg.DeleteMsg);
                        vResult = true;
                    }
                    else
                    {
                        gblFuction.MsgPopup(gblMarg.DBError);
                        vResult = false;
                    }
                }
                return vResult;
            }
            finally
            {
                oLg = null;
                oGbl = null;
            }
        }
